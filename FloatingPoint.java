class FloatingPoint{
    public static void main(String[] args)
    {
        float myFloatNum=5.3f;
        int myFloatNum1=34;
        double myFloatNum2=234.3838;
        System.out.println(myFloatNum);
        System.out.println(myFloatNum1);
        System.out.println(myFloatNum2);
        
    }
}